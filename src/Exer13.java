
public class Exer13 {

	public static void main(String[] args) {
		int nove = 9;
		int dez = 10;
		
		System.out.println( dez < 8 && dez > nove ); // false // AND
		System.out.println(dez < 9 || dez < 8);//false  // OR
		System.out.println(dez > 9);//true
		System.out.println(!(dez>9));//false // NOT

	}

}
