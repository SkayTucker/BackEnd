

public class Exer12 {

	public static void main(String[] args) {
		int a = 10;
		int b = 20;
		
		
		boolean result = a < b;
		System.out.println(result);
		
		result = a > b;
		System.out.println(result);
		
		result = a == b;
		System.out.println(result);
		
		result = a != b;
		System.out.println(result);
		
		System.out.println(a >= b);
		System.out.println(a <= b);
			
	}

}
